# Check the score of your exams
score = int(input("Enter your score: "))
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Keep Practicing More")

# Check if the number is odd or even
num = int(input("Enter a number: "))
if num % 2 == 0:
    print("Even")
else: 
    print("Odd")

# Check if the number is positive, negative or zero
